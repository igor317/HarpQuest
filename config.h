/**
 * config.h - Файл настроек для игры HarpQuest
 * 
 * В этом файле собраны все настройки игры - от пинов подключения
 * до времени ожидания и громкости звука. Изменяя значения здесь,
 * можно настроить игру под разные условия.
 */

#ifndef CONFIG_H
#define CONFIG_H

// === НАСТРОЙКИ ОТЛАДКИ ===

// Включает вывод отладочных сообщений в последовательный порт
// Чтобы отключить отладку, нужно закомментировать эту строку
#define DEBUG_LOG

// === НАСТРОЙКИ ПАМЯТИ ===

// Адрес в EEPROM (энергонезависимой памяти), откуда начинается запись игровой последовательности
#define EEPROM_DATA_START_ADDRESS 0

// === НАСТРОЙКИ КНОПОК ===

// Время в миллисекундах, которое нужно удерживать кнопку для перехода в режим калибровки
#define CALIBRATION_MODE_TIMEOUT 2000

// Время в миллисекундах, через которое сработает кнопка администратора
#define ADMIN_BUTTON_TIMEOUT 1000

// === НАСТРОЙКИ СТРУН АРФЫ ===

// Режим работы фоторезисторов:
// Если строка раскомментирована - фоторезисторы считаются активными при попадании света
// Если строка закомментирована - фоторезисторы считаются активными при отсутствии света
#define HARPSTRING_HIGHLIGHTED_DEFAULT

// Порог срабатывания струны (чувствительность) - значение от 0 до 1023
// Чем меньше значение, тем чувствительнее струны
#define HARPSTIRNG_THRESHOLD  50

// Количество струн в арфе
#define NUM_HARPSTRINGS 5

// === НАСТРОЙКИ ПОДКЛЮЧЕНИЯ ===

// Пин, к которому подключена кнопка игрока
#define BUTTON_PIN  8

// Пин, к которому подключена кнопка администратора
#define ADMIN_BUTTON_PIN 7

// Пин, к которому подключен замок (или реле для замка)
#define LOCKER_PIN 13

// Пин, к которому подключен сигнал "Busy" MP3-плеера
#define MP3_BUSY_PIN 2

// === НАСТРОЙКИ ЗВУКА ===

// Громкость звука (от 0 до 30)
#define SOUND_VOLUME 25

// === НАСТРОЙКИ ИГРЫ ===

// Максимальная длина последовательности, которую нужно запомнить
#define MAX_SEQUENCE_LENGTH 20

// Время в миллисекундах ожидания перед воспроизведением введенной последовательности
#define WAIT_AFTER_END_SEQUENCE 1000

// === НАСТРОЙКИ ЗВУКОВЫХ СИГНАЛОВ ===

// Номер звукового файла, который проигрывается при начале калибровки
#define SAMPLE_START_CALIBRATION 7

// Номер звукового файла, который проигрывается при завершении калибровки
#define SAMPLE_END_CALIBRAION 8

// === НАСТРОЙКИ ТАЙМАУТОВ ===

// Время в миллисекундах, если в течение этого времени не поступит новая команда, 
// то режим калибровки автоматически завершится
#define END_CALIBRATION_INTERVAL 5000

// Время в миллисекундах, через которое после успешного завершения
// игра возвращается в нормальное состояние
#define FINISH_GAME_TIMEOUT 5000

#endif